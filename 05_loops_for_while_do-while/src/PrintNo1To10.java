public class PrintNo1To10 {
    public static void main(String[] args){

        for(int i = 10; i >= 1; i--){
            System.out.println(i);
        }

        for(int i = 1; i <= 10; i++){
            System.out.println(i);
        }

        for(int i = 5; i <= 50; i+=5){
            System.out.println(i);
        }

    }
}
